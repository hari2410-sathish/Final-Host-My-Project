// =======================================
// ðŸ”¥ FINAL PRODUCTION-READY SERVER.JS
// =======================================

require("dotenv").config();
const validateEnvironment = require("./src/config/envValidator");

// ============================
// VALIDATE ENVIRONMENT
// ============================
validateEnvironment();

const express = require("express");
const cors = require("cors");
const cookieParser = require("cookie-parser");
const session = require("express-session");
const http = require("http");
const jwt = require("jsonwebtoken");
const { Server } = require("socket.io");
const helmet = require("helmet");

// ============================
// ROUTES
// ============================
const adminRoutes = require("./src/routes/adminRoutes");
const userRoutes = require("./src/routes/userRoutes");
const studentRoutes = require("./src/routes/studentRoutes");
const notificationRoutes = require("./src/routes/notificationRoutes");

// ============================
// APP
// ============================
const app = express();

// ============================
// ðŸ”¥ CORS â€” EC2 FIXED
// ============================
const FRONTEND_URL = "http://54.226.206.161";

app.use(
  cors({
    origin: FRONTEND_URL,
    credentials: true,
    methods: ["GET", "POST", "PUT", "DELETE", "OPTIONS"],
    allowedHeaders: ["Content-Type", "Authorization"],
  })
);

// Preflight
app.options("*", cors());

// ============================
// SECURITY
// ============================
app.use(helmet());

// ============================
// BODY PARSERS
// ============================
app.use(express.json());
app.use(express.urlencoded({ extended: true }));
app.use(cookieParser());

// ============================
// STATIC FILES
// ============================
app.use("/uploads", express.static("uploads"));

// ============================
// SESSION (HTTP SAFE)
// ============================
app.use(
  session({
    secret: process.env.SESSION_SECRET || "supersecret_session_key",
    resave: false,
    saveUninitialized: false,
    cookie: {
      httpOnly: true,
      secure: false,      // â— HTTP (no HTTPS)
      sameSite: "lax",    // â— required for cross-origin
      maxAge: 24 * 60 * 60 * 1000,
    },
  })
);

// ============================
// HEALTH CHECK
// ============================
app.get("/health", (req, res) => {
  res.json({ success: true, message: "API is running" });
});

// ============================
// API ROUTES
// ============================
app.use("/auth/user", userRoutes);
app.use("/admin", adminRoutes);

const courseAccessRoutes = require("./src/routes/courseAccessRoutes");
app.use("/api/students", studentRoutes);
app.use("/courses", courseAccessRoutes);
app.use("/courses", require("./src/routes/courseRoutes"));
app.use("/api/notifications", notificationRoutes);
app.use("/payments", require("./src/routes/paymentRoutes"));
app.use("/api/chat", require("./src/routes/chatRoutes"));

// ============================
// GLOBAL ERROR HANDLER
// ============================
app.use((err, req, res, next) => {
  console.error("âŒ GLOBAL ERROR:", err);
  res.status(err.status || 500).json({
    success: false,
    message: err.message || "Server error",
  });
});

// ============================
// HTTP SERVER
// ============================
const server = http.createServer(app);

// ============================
// SOCKET.IO (EC2 FIXED)
// ============================
const io = new Server(server, {
  cors: {
    origin: FRONTEND_URL,
    credentials: true,
  },
});

app.set("io", io);

// ============================
// SOCKET AUTH
// ============================
io.use((socket, next) => {
  try {
    const token =
      socket.handshake.auth?.token ||
      socket.handshake.headers?.authorization?.split(" ")[1];

    if (!token) return next(new Error("Auth token missing"));

    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    socket.user = decoded;
    next();
  } catch (err) {
    console.error("âŒ Socket Auth Error:", err.message);
    next(new Error("Invalid token"));
  }
});

// ============================
// SOCKET EVENTS
// ============================
io.on("connection", (socket) => {
  const userId = socket.user?.id;

  console.log(`ðŸ”Œ Socket Connected: ${socket.id} | User: ${userId}`);

  if (userId) socket.join(`user:${userId}`);

  socket.on("join_room", (room) => socket.join(room));
  socket.on("leave_room", (room) => socket.leave(room));

  socket.on("chat_message", ({ room, content, type = "text" }) => {
    io.to(room).emit("receive_message", {
      id: Date.now().toString(),
      senderId: userId,
      senderName: socket.user?.name || "User",
      content,
      type,
      timestamp: new Date(),
    });
  });

  socket.on("typing", ({ room, isTyping }) => {
    socket.to(room).emit("user_typing", { userId, isTyping });
  });

  socket.on("disconnect", () => {
    console.log(`âŒ Socket Disconnected: ${socket.id}`);
  });
});

// ============================
// START SERVER
// ============================
const PORT = process.env.PORT || 5000;
server.listen(PORT, () => {
  console.log(`ðŸš€ Server running at http://0.0.0.0:${PORT}`);
});

// ============================
// BACKGROUND JOBS
// ============================
const startEmailRetryJob = require("./src/jobs/emailRetryJob");
startEmailRetryJob();
